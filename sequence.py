# list
list1 = ["apple", 5, 6.7, "orange"]
print(list1)
print(list1[0])
print(list1[2:4])
print(type(list1))

# Tuples
mytuple = ("Mary", "Sophie", 10)
print(type(mytuple))
print(mytuple[1])
print(mytuple[0:2])
print(mytuple*2)

# dictionaries comes with key and value
examp = {1: "phone", 2:"laptop", 3:"computer"}
print(type(examp))
print(examp.keys())
print(examp.values())
print(examp[1])
print(examp[3])